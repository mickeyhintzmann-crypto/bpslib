#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const publicRoot = path.join(repoRoot, "public");
const manifestOut = path.join(repoRoot, "lib", "mediaManifest.ts");

const IMAGE_EXTENSIONS = new Set([".jpg", ".jpeg", ".png", ".webp", ".heic", ".heif", ".avif"]);

const roots = {
  gulv: [
    path.join(repoRoot, "public", "media", "galleries", "gulv", "before-after"),
    path.join(repoRoot, "public", "media", "galleries:gulv:before-after")
  ],
  bordplade: [
    path.join(repoRoot, "public", "media", "galleries", "bordplade", "before-after"),
    path.join(repoRoot, "public", "media", "galleries:bordplade:before-after")
  ]
};

function isDirectory(targetPath) {
  try {
    return fs.statSync(targetPath).isDirectory();
  } catch {
    return false;
  }
}

function toWebPath(filePath) {
  const rel = path.relative(publicRoot, filePath).split(path.sep).join("/");
  return `/${rel}`;
}

function isImage(filePath) {
  return IMAGE_EXTENSIONS.has(path.extname(filePath).toLowerCase());
}

function sorted(list) {
  return [...list].sort((a, b) => a.localeCompare(b, "da", { numeric: true, sensitivity: "base" }));
}

function pickBeforeAfter(imageFiles) {
  if (imageFiles.length < 2) {
    return null;
  }

  const beforeRegex = /(^|[^a-z0-9])before([^a-z0-9]|$)/i;
  const afterRegex = /(^|[^a-z0-9])after([^a-z0-9]|$)/i;

  const beforeCandidates = imageFiles.filter((filePath) => beforeRegex.test(path.basename(filePath)));
  const afterCandidates = imageFiles.filter((filePath) => afterRegex.test(path.basename(filePath)));

  if (beforeCandidates.length && afterCandidates.length) {
    return {
      before: beforeCandidates[0],
      after: afterCandidates[0]
    };
  }

  return {
    before: imageFiles[0],
    after: imageFiles[imageFiles.length - 1]
  };
}

function collectBeforeAfter(rootCandidates) {
  const results = [];
  const seen = new Set();

  for (const root of rootCandidates) {
    if (!isDirectory(root)) {
      continue;
    }

    const caseDirs = sorted(
      fs
        .readdirSync(root, { withFileTypes: true })
        .filter((entry) => entry.isDirectory())
        .map((entry) => path.join(root, entry.name))
    );

    for (const caseDir of caseDirs) {
      const imageFiles = sorted(
        fs
          .readdirSync(caseDir, { withFileTypes: true })
          .filter((entry) => entry.isFile())
          .map((entry) => path.join(caseDir, entry.name))
          .filter(isImage)
      );

      const pair = pickBeforeAfter(imageFiles);
      if (!pair) {
        continue;
      }

      const record = {
        caseId: path.basename(caseDir),
        beforeSrc: toWebPath(pair.before),
        afterSrc: toWebPath(pair.after)
      };

      const dedupeKey = `${record.caseId}|${record.beforeSrc}|${record.afterSrc}`;
      if (seen.has(dedupeKey)) {
        continue;
      }
      seen.add(dedupeKey);
      results.push(record);
    }
  }

  return sorted(results.map((item) => JSON.stringify(item))).map((item) => JSON.parse(item));
}

function buildGallery(beforeAfterList) {
  const seen = new Set();
  const gallery = [];

  for (const item of beforeAfterList) {
    if (!seen.has(item.afterSrc)) {
      seen.add(item.afterSrc);
      gallery.push(item.afterSrc);
    }
  }

  return gallery;
}

function writeManifest({ beforeAfterGulv, beforeAfterBordplade, galleryGulv, galleryBordplade }) {
  const content = `/* eslint-disable */\n// Auto-generated by scripts/generate-media-manifest.mjs\n// Do not edit manually.\n\nexport type BeforeAfterItem = {\n  caseId: string;\n  beforeSrc: string;\n  afterSrc: string;\n};\n\nexport const beforeAfterGulv: BeforeAfterItem[] = ${JSON.stringify(beforeAfterGulv, null, 2)};\n\nexport const beforeAfterBordplade: BeforeAfterItem[] = ${JSON.stringify(beforeAfterBordplade, null, 2)};\n\nexport const galleryGulv: string[] = ${JSON.stringify(galleryGulv, null, 2)};\n\nexport const galleryBordplade: string[] = ${JSON.stringify(galleryBordplade, null, 2)};\n`;

  fs.writeFileSync(manifestOut, content + "\n", "utf8");
}

const beforeAfterGulv = collectBeforeAfter(roots.gulv);
const beforeAfterBordplade = collectBeforeAfter(roots.bordplade);
const galleryGulv = buildGallery(beforeAfterGulv);
const galleryBordplade = buildGallery(beforeAfterBordplade);

writeManifest({ beforeAfterGulv, beforeAfterBordplade, galleryGulv, galleryBordplade });

console.log(
  `[media-manifest] Gulv: ${beforeAfterGulv.length} pairs, Bordplade: ${beforeAfterBordplade.length} pairs`
);
