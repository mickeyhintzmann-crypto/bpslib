#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const logosDir = path.join(repoRoot, "public", "media", "logos", "clients");
const outputPath = path.join(repoRoot, "lib", "clientLogos.ts");

const SUPPORTED_EXTENSIONS = new Set([".svg", ".png"]);

function titleCase(input) {
  return input
    .split(" ")
    .filter(Boolean)
    .map((word) => word.charAt(0).toUpperCase() + word.slice(1).toLowerCase())
    .join(" ");
}

function buildAlt(fileName) {
  const ext = path.extname(fileName);
  const baseName = fileName.slice(0, -ext.length);
  const normalized = baseName
    .replace(/[.-]+/g, " ")
    .replace(/\s+/g, " ")
    .trim();

  const withoutTrailingLogo = normalized.replace(/\blogo\b$/i, "").trim();
  const prettyName = titleCase(withoutTrailingLogo || normalized);
  return `${prettyName} logo`;
}

if (!fs.existsSync(logosDir)) {
  console.error(`[client-logos] Missing directory: ${logosDir}`);
  process.exit(1);
}

const files = fs
  .readdirSync(logosDir, { withFileTypes: true })
  .filter((entry) => entry.isFile())
  .map((entry) => entry.name)
  .filter((name) => SUPPORTED_EXTENSIONS.has(path.extname(name).toLowerCase()))
  .sort((a, b) => a.localeCompare(b, "da", { sensitivity: "base" }));

const logos = files.map((fileName) => ({
  src: `/media/logos/clients/${encodeURIComponent(fileName)}`,
  alt: buildAlt(fileName)
}));

const content = `/* eslint-disable */\n// Auto-generated by scripts/generate-client-logos.mjs\n// Do not edit manually.\n\nexport const clientLogos = ${JSON.stringify(logos, null, 2)} as const;\n`;

fs.writeFileSync(outputPath, `${content}\n`, "utf8");

console.log(`[client-logos] logos found: ${logos.length}`);
